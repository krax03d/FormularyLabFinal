package com.example.formularylab.Entidades;

public class Formulas {

    private Integer id;
    private String nombre;


    public Formulas() {
        this.id = id;
        this.nombre = nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Integer getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }





}

